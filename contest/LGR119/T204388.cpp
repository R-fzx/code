using namespace std;
using LL = long long;
using Pii = pair<int, int>;
using Pll = pair<LL, LL>;

const int kN = 5e5 + 1;

int n, a[kN], b[kN];
LL c[2];

int main() {
  ios_base::sync_with_stdio(0), cin.tie(0), cout.tie(0);
  cin >> n;
  for (int i = 1; i <= n; ++i) {
    cin >> a[i];
  }
  for (int i = 1; i <= n; ++i) {
    cin >> b[i];
    c[0] += a[i] != b[i], c[1] += a[n - i + 1] != b[i];
  }
  cout << min(c[0], c[1] + 1);
  return 0;
}