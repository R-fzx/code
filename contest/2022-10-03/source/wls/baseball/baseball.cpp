inline int read() {
    int x = 0, f = 1; char c = getchar();
    while (!isdigit(c)) { if (c == '-') f = -1; c = getchar(); }
    while (isdigit(c)) x = x * 10 + c - '0', c = getchar();
    return x * f;
}
void print(int x) {
    if (x < 0) putchar('-'), x = -x;
    if (x > 9) print(x / 10);
    putchar(x % 10 + '0');
}
int n;
double r;
double florr(double x, int powd) { // pow(10, d)
    return floor(x * powd + 0.5) / powd;
}
signed main() {
    freopen("baseball.in", "r", stdin);
    freopen("baseball.out", "w", stdout);
    while (~scanf("%d", &n)) {
        scanf("%lf", &r);
        int base = pow(10, n);
        int b = base, a = r * b, g = __gcd(a, b); a /= g, b /= g;
        int ans = b;
        b = 1;
        while (b < base) {
            a = (int)(b * r + 0.5);
            double r2 = (double)(a) / b;
            if (florr(r2, base) == r) {
                ans = b;
                break;
            }
            ++b;
        }
        println(ans);
        // printf("%d/%d\n", a, b);
    }
    return 0;
}