using namespace std;
using LL = long long;
using Pii = pair<int, int>;
using Pll = pair<LL, LL>;

const int kN = 101;

int n;
double f[kN];

int main() {
  ios_base::sync_with_stdio(0), cin.tie(0), cout.tie(0);
  cin >> n;
  for (int i = 1; i <= n; ++i) {
    for (int j = 1; j <= 6; ++j) {
      f[i] += max(j * 1.0, f[i - 1]);
    }
    f[i] /= 6;
  }
  cout << fixed << setprecision(10) << f[n];
  return 0;
}